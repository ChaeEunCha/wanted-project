/**
 * 특정 skill_tag_id에 대한 설명을 조회한다.
 * 사전에 없으면 undefined를 반환한다 (호출 측에서 "툴팁 미노출"을 그대로 구현할 수 있게).
 */
export function getTermDescription(skillTagId: number): string | undefined {
  return TERM_GLOSSARY[skillTagId];
}

/** 스킬 태그 하나에 설명이 있는지 여부 (툴팁 노출 여부 판단용) */
export function hasTermDescription(skillTagId: number): boolean {
  return Object.prototype.hasOwnProperty.call(TERM_GLOSSARY, skillTagId);
}

/**
 * 개발용 커버리지 체크 헬퍼: MOCK_SKILL_TAGS 중 사전에 등록된 것/안 된 것을 나눠 반환한다.
 * (데모 페이지나 추후 P6 연동 시 우선순위 파악에 참고용 — 런타임 필수 로직은 아님)
 */
export function getGlossaryCoverage(): {
  covered: SkillTag[];
  uncovered: SkillTag[];
} {
  const covered: SkillTag[] = [];
  const uncovered: SkillTag[] = [];
  for (const tag of MOCK_SKILL_TAGS) {
    (hasTermDescription(tag.id) ? covered : uncovered).push(tag);
  }
  return { covered, uncovered };
}
